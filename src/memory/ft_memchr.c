#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned char	value;

	ptr = (unsigned char *)s;
	value = (unsigned char)c;
	while (n > 0)
	{
		if (*ptr == value)
			return (ptr);
		ptr++;
		n--;
	}
	return (NULL);
}
